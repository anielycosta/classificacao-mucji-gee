var buffer = ee.FeatureCollection('ft:1Tpc9l7W0DprPGDwmfmXs-8LYZDX9hwEr5R0IIVaE')
Map.addLayer(buffer);

var filtro = imageCollection.filterBounds(buffer)
    .filterMetadata('CLOUD_COVER', 'less_than', 0.9)
    .filterDate('1998-07-01','2000-01-30');

//Map.addLayer(filtro);

var img = ee.Image('LANDSAT/LT05/C01/T1_SR/LT05_219077_19991216');

var visParams = {bands: ['B7', 'B5', 'B3'], min: 25, max: 3538}

Map.addLayer(img, visParams, 'imagem');


var amostra = vegetacao.merge(agua).merge(urbano).merge(agricultura);
//Map.addLayer(amostra);

var bandas = ['B1', 'B2', 'B3', 'B4', 'B5', 'B6', 'B7'];

var treinamento =  img.select(bandas).sampleRegions({
 collection: amostra,
 properties: ['class'],
 scale: 30
});

var classificacao = ee.Classifier.cart().train({
  features: treinamento,
  classProperty: 'class', 
  inputProperties: bandas
})

var classificacao2 = img.select(bandas).classify(classificacao);
Map.addLayer(classificacao2, {min: 0, max: 3, palette:['green', 'blue', 'yellow', 'magenta']},'classificao');

Export.image.toDrive({  
 image: classificacao2,  
 description: 'class2_cart_1999',  
 scale: 30,
 region: ROI,
 fileFormat: 'GeoTIFF',  
 maxPixels: 1e12,  
})
